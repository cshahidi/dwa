<h2>About Us</h2>
<h3>
	Portside Capital Holdings LLC is a private investment company that identifies 
	opportunities in distressed residential real estate for acquisition. 
</h3> <br>
<h3>INVESTMENT STRATEGY</h3>
<p>
	Portside Capital Holdings LLC is focused on investments that offer relatively attractive 
	risk-adjusted returns. We seek properties at deep discounts to market value, 
	such as foreclosures. Portside will be funded by a pool of private money investors, 
	both active and passive. The principals will be actively involved in managing the real estate 
	portfolio.
</p> 
<h3>PARTNER RELATIONSHIP</h3>
<p>
	Portside Capital Holdings LLC owes much of its success to our partners. Partners are direct sellers
	and referring parties (Bird dogs) who fetch attractive property deals.  Those leads are entered 
	into our lead generation and tracking system to be qualified.  At closing, Portside rewards our 
	partners with referral fees commensurate with the potential profit on that deal.
</p>

